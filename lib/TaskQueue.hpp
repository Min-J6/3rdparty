class TaskQueue {
public:
    TaskQueue() : running(true) {}

    TaskQueue(const TaskQueue&) = delete;
    TaskQueue& operator=(const TaskQueue&) = delete;

    template <typename F>
    void post(F&& f) {

        auto task_generator = [task = std::forward<F>(f)]() -> std::function<void()> {
            return task;
        };

        std::lock_guard<std::mutex> lock(queue_mutex);
        if (!running) return;
        tasks.emplace(std::move(task_generator));
    }

    // 2. 조건부 실행 작업을 추가합니다.
    template <typename C, typename F>
    void post_conditional(C&& condition, F&& f) {
        auto task_generator = [cond = std::forward<C>(condition),
                               task = std::forward<F>(f)]() -> std::function<void()> {
            if (cond()) {
                return task; // 조건 충족 시 실제 작업 반환
            } else {
                return nullptr; // 조건 미충족 시 nullptr 반환 (나중에 다시 시도)
            }
        };

        std::lock_guard<std::mutex> lock(queue_mutex);
        if (!running) return;
        tasks.emplace(std::move(task_generator));
    }


    void stop() {
        std::lock_guard<std::mutex> lock(queue_mutex);
        running = false;
    }

    size_t size() {
        std::lock_guard<std::mutex> lock(queue_mutex);
        return tasks.size();
    }

    bool run_one() {
        std::function<void()> actual_task_to_run; // 실제 실행할 작업

        {
            std::lock_guard<std::mutex> lock(queue_mutex);

            if (!running || tasks.empty()) {
                return false;   // 작업이 없음
            }


            auto& task_generator = tasks.front(); // 맨 앞의 작업 작업 생성기 가져오기
            actual_task_to_run = task_generator();


            if (actual_task_to_run) {
                tasks.pop();
            } else {
                return false; // 실행한 작업이 없음
            }
        } // 락 해제


        actual_task_to_run(); // 작업 실행
        return true; // 작업 실행함
    }

private:
    std::queue<std::function<std::function<void()>()>> tasks;
    std::mutex queue_mutex;
    bool running;
};


