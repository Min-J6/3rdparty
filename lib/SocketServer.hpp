#include <boost/asio/streambuf.hpp>
#include <boost/regex.hpp>

using boost::asio::ip::tcp;

class Session : public std::enable_shared_from_this<Session> {
public:
    using Callback_receive = std::function<void(std::shared_ptr<Session>, const std::string&)>;
    using Callback_error = std::function<void(std::shared_ptr<Session>, const boost::system::error_code&)>;


    Callback_receive on_receive;                // 데이터를 받을 때 호출됨
    Callback_error on_error;                    // 에러 발생시 호출됨


    void start();                               // 서버 시작

    void send(const std::string& message);      // 비동기 데이터 전송
    void send(const std::vector<char>& data);   // 비동기 데이터 전송

    std::string ip();                           // 클라이언트 IP 가져오기
    tcp::socket& socket() { return socket_; }   // 클라이언트 소켓 가져오기


    // 세션 팩토리 메소드
    static std::shared_ptr<Session> create(boost::asio::io_context& io_context) {
        return std::shared_ptr<Session>(new Session(io_context));
    }


private:
    Session(boost::asio::io_context& io_context)
        : socket_(io_context),
          delimiter_regex_("(\r\n|\r|\n)")
    {}


    // 비동기 데이터 읽기
    void do_read();


    // 로그 출력
    void backlog(const std::string& log) {
        std::cout << "[Backlog] [Session] " << log << std::endl;
    }



    tcp::socket socket_;
    boost::asio::streambuf buffer_;
    boost::regex delimiter_regex_;
};



class Server {
public:
    std::function<void(std::shared_ptr<Session>)> on_accept;
    std::function<void(const boost::system::error_code&)> on_error;

    Server(short port) : acceptor_(io_context_, tcp::endpoint(tcp::v4(), port)) {

        backlog("서버 포트 열림: " + std::to_string(port));

        do_accept();
    }


    // 서버 시작
    void run() {
        io_context_.run();
    }

private:
    // 비동기 클라이언트 연결
    void do_accept();


    // 로그 출력
    void backlog(const std::string& msg) {
        std::cout << "[BackLog] [Server] " << msg << std::endl;
    }

    boost::asio::io_context io_context_;
    tcp::acceptor acceptor_;
};




