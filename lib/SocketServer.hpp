inline void Session::start()
{

    backlog("[Session] 클라이언트 연결됨: " + socket_.remote_endpoint().address().to_string());

    do_read();
}



inline void Session::send(const std::string &message)
{
    auto self = shared_from_this();
    boost::asio::async_write(socket_, boost::asio::buffer(message),
                 [this, self](boost::system::error_code ec, std::size_t /*length*/)
                 {
                     if (ec) {
                         on_error ? on_error(self, ec)
                             : backlog("전송 실패: " + ec.message());
                     }
                 });
}



inline void Session::send(const std::vector<char> &data)
{
    auto self = shared_from_this();
    boost::asio::async_write(socket_, boost::asio::buffer(data),
                 [this, self](boost::system::error_code ec, std::size_t /*length*/)
                 {
                     if (ec) {
                         on_error ? on_error(self, ec)
                             : backlog("전공 실패: " + ec.message());
                     }
                 });
}

inline std::string Session::ip()
{
    return socket_.remote_endpoint().address().to_string();
}


inline void Session::do_read()
{
    auto self = shared_from_this();

    // '\r', '\n', '\r\n'가 나올때까지 읽기
    boost::asio::async_read_until(socket_, buffer_, delimiter_regex_,
                  [this, self](boost::system::error_code ec, std::size_t length)
                  {
                      if (!ec) {
                          // 데이터 읽기 성공
                          const char* data_ptr = boost::asio::buffer_cast<const char*>(buffer_.data());
                          std::vector<char> bytes_data(data_ptr, data_ptr + length);


                          on_receive ? on_receive(self, bytes_data)
                              : backlog("데이터 수신: " + std::string(bytes_data.begin(), bytes_data.end()));


                          buffer_.consume(length);
                          do_read();
                      }
                      else if (ec == boost::asio::error::eof) {
                          // 정상 종료됨
                          backlog("클라이언트 연결 종료: " + socket_.remote_endpoint().address().to_string());
                      }
                      else {
                          // 에러 발생함
                          on_error ? on_error(self, ec)
                              : backlog("수신 실패: " + ec.message());
                      }
                  });
}



inline void Server::do_accept()
{
    auto new_session = Session::create(io_context_);

    acceptor_.async_accept(new_session->socket(),
                   [this, new_session](boost::system::error_code ec)
                   {
                       if (ec) {
                           // 연결 실패
                           on_error ? on_error(ec)
                               : backlog("Accept 에러: " + ec.message());
                       }
                       else {
                           // 연결 성공
                           new_session->start();

                           on_accept ? on_accept(new_session)
                               : backlog("클라이언트 연결됨: " + new_session->socket().remote_endpoint().address().to_string());
                       }

                       do_accept();
                   });
}
